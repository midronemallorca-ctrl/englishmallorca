/* ============================================================
   Language switching
   ============================================================ */
let currentLang = localStorage.getItem('em_lang') || 'es';

function applyLang(lang) {
  currentLang = lang;
  const t = translations[lang];

  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t[el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = t[el.dataset.i18nHtml];
    if (v !== undefined) el.innerHTML = v;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const v = t[el.dataset.i18nPlaceholder];
    if (v !== undefined) el.placeholder = v;
  });

  const esOpt = document.getElementById('lang-es');
  const caOpt = document.getElementById('lang-ca');
  if (esOpt && caOpt) {
    esOpt.classList.toggle('lang-opt--active', lang === 'es');
    caOpt.classList.toggle('lang-opt--active', lang === 'ca');
  }

  localStorage.setItem('em_lang', lang);
}

/* ---- Language toggle init ---- */
(function () {
  const toggle = document.getElementById('lang-toggle');
  if (!toggle) return;
  toggle.addEventListener('click', () => {
    applyLang(currentLang === 'es' ? 'ca' : 'es');
  });
  applyLang(currentLang);
})();

